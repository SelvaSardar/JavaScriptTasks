for (let i=1; i<=10; i++)//For Loop is Condition Increment.
{
    console.log("The Increment of i is ",i);
}

for (let i=10; i>=1; i--)//For Loop is Condition Decrement.
{
    console.log("The Decrement of i is",i);
}

let x=1;
while (x<=10)//While Loop is Condition Increment.
{
    console.log("The Whileloop Increment of x is ",x);
    x++;
}

let z=10;
while (z>=1)//While Loop is Condition Decrement.
{
    console.log("The Whileloop Decrement of z is ",z);
    z--;
}

let y=6;
do {
    console.log("The do-while y is ",y);
    y++;
}while (y<=9)//The Condition is Put Down Last.

let q=10;
do {
    console.log("The do-while q is ",q);
    q--;
}while(q>=4)//Decrement.                  